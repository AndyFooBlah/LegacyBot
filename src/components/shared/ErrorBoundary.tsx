/**
 * ErrorBoundary — catches unhandled React rendering errors.
 *
 * Displays a friendly, non-technical error message rather than a
 * white screen. This is especially important for the Storyteller
 * experience — they should never see a raw stack trace.
 *
 * References: product_requirements.md §4 (Safety) | GitHub Issue #19
 */

import React, { Component, ErrorInfo, ReactNode } from 'react';

interface Props {
  children: ReactNode;
}

interface State {
  hasError: boolean;
}

export class ErrorBoundary extends Component<Props, State> {
  constructor(props: Props) {
    super(props);
    this.state = { hasError: false };
  }

  static getDerivedStateFromError(_: Error): State {
    return { hasError: true };
  }

  componentDidCatch(error: Error, errorInfo: ErrorInfo) {
    console.error('[ErrorBoundary] Caught error:', error, errorInfo);
  }

  render() {
    if (this.state.hasError) {
      return (
        <div className="min-h-screen flex items-center justify-center bg-slate-50 p-6">
          <div className="bg-white p-8 rounded-3xl shadow-2xl max-w-md text-center space-y-6">
            <h2 className="text-2xl font-bold text-slate-800">
              Something went wrong
            </h2>
            <p className="text-slate-500">
              Don&apos;t worry — your stories are safe. Please refresh the page
              to continue.
            </p>
            <button
              onClick={() => window.location.reload()}
              className="w-full py-4 bg-indigo-600 text-white rounded-2xl font-bold hover:bg-indigo-700 transition-colors"
            >
              Refresh
            </button>
          </div>
        </div>
      );
    }

    return this.props.children;
  }
}
